import { useUsers } from '../hooks/useUsers'

function ViewUser() {
  // Use the query hook
  const { data: users, isLoading, isError, error } = useUsers()

  if (isLoading) {
    return <div>Loading...</div>
  }

  if (isError) {
    return <div>Error: {error.message}</div>
  }

  return (
    <div>
      <h1>Users</h1>
      <ul>
        {users.map(user => (
          <li key={user.id}>
            {user.name} - {user.email}
          </li>
        ))}
      </ul>
    </div>
  )
}

export default ViewUser 